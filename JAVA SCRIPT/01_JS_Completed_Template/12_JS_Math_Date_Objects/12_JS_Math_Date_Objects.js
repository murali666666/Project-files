/*
    Math Object Examples


 */

// Math PI and Sqrt
var pi= Math.PI;
console.log(pi);
console.log(Math.sqrt(4));

// find the min of 4 numbers
var minValue=Math.min(3,4,5,6);
var output="the minimum value of 3,4,5,6 is " +minValue;
console.log(output);

// find the Max of 4 numbers
var maxValue=Math.max(3,4,5,6);
output="the maximum value of 3,4,5,6 is " +maxValue;
console.log(output);

// find the 'x' to the power of 'y' value, ex: 2^4
var power=Math.pow(2,4);
output="2 to the power of 4 is "+power;
console.log(output);

// generate the random numbers from 0 to 100
var randomNum=Math.round(Math.random()*1000);
console.log(randomNum);


